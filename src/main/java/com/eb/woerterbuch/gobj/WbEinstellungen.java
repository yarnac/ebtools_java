package com.eb.woerterbuch.gobj;


public class WbEinstellungen {
	private String googleWeb;
	private String googleBilder;
	private String googleSprache;
	private String verbix;
	
	
	public String getGoogleWeb() {
		return googleWeb;
	}
	public void setGoogleWeb(String googleWeb) {
		this.googleWeb = googleWeb;
	}
	public String getGoogleBilder() {
		return googleBilder;
	}
	public void setGoogleBilder(String googleBilder) {
		this.googleBilder = googleBilder;
	}
	public String getGoogleSprache() {
		return googleSprache;
	}
	public void setGoogleSprache(String googleSprache) {
		this.googleSprache = googleSprache;
	}
	public String getVerbix() {
		return verbix;
	}
	public void setVerbix(String verbix) {
		this.verbix = verbix;
	}

}
